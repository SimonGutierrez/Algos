// slope = 3/1 find how many trees you will encounter when starting at top left

function countTrees(input, right = 3, down = 1){
    let count = 0;
    let col = 0;

    for (let i = down; i < input.length; i += down) {
      col += right;

      if (col >= input[i].length) {
        col = col - input[i].length;
      }

      let currPoint = input[i][col];

      if (currPoint === '#') {
        count++;
      }

    }

    return count;
  }

  function multipleTrees(input){
    let slopes = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]
    let count = 1;

    slopes.forEach((elem) => count *= countTrees(input, elem[0], elem[1]));

    return count;
  }


